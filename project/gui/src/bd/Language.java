    public ArrayList<SetterLanguage> tableList(){
        
        ArrayList<SetterLanguage> tableList = new ArrayList<>();
        Connection con = (new GetConnection()).getConnection();
        String query = "select * from language ";
        Statement st;
        ResultSet rs;
        
        try{
            st = con.createStatement();
            rs = st.executeQuery(query);
            SetterLanguage setter;
            while(rs.next()){
                setter = new SetterLanguage(rs.getString("lng_gui_AT"),rs.getString("lng_language"));
                tableList.add(setter);
            }
        }catch(Exception ex){
            ex.printStackTrace();
        }
        return tableList;
    }
    
        private void DisplayTable(){
        ArrayList<SetterLanguage> list = tableList();
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        Object []  row = new Object [2];
        for(int i=0; i<list.size(); i++){
            row[0]= list.get(i).getAT();
            row[1]= list.get(i).getLanguage();

            model.addRow(row);
        }
    }
    
    
    public void executeQuery(String query, String message){
        Connection con = (new GetConnection()).getConnection();
        Statement st;
        
        try{
            st = con.createStatement();
            if((st.executeUpdate(query))==1){
                DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
                model.setRowCount(0);
                DisplayTable();
                JOptionPane.showMessageDialog(null, "Data "+message+"Succesfully");
            }else{
                JOptionPane.showMessageDialog(null, "Data Not"+message);
            }
        }catch(Exception ex){
            ex.printStackTrace();
        }
           
    }        


