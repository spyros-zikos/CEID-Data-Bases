    private void tableList(){
   
        Connection con = (new GetConnection()).getConnection();
     
        String query = "select action_taken, who, chosentable, cur_datetime from log; " ;

        Statement st;
        ResultSet rs;
        
        try{
            st = con.createStatement();
            rs = st.executeQuery(query);

            while(rs.next()){ 
                String data1 = rs.getString("action_taken");
                String data2 = rs.getString("who");
                String data3 = rs.getString("chosentable");
                String data4 = rs.getString("cur_datetime");                         
             
                Object[] row = { data1, data2, data3, data4};

                DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

                model.addRow(row);
            }
  
        }catch(Exception ex){
            ex.printStackTrace();
        }
    }    

