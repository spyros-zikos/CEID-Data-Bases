    private void tableList(){
   
        Connection con = (new GetConnection()).getConnection();
     
        String query = "call SearchTrip("+jTextField1.getText()+", '"+jTextField2.getText()+"', '"+jTextField3.getText()+"');";

        Statement st;
        ResultSet rs;
        
        try{
            st = con.createStatement();
            rs = st.executeQuery(query);
            int count=0;
            if(rs.next()){
                while(rs.next()){ 
           
                    int data1 = rs.getInt("tr_id1");
                    float data2 = rs.getFloat("tr_cost");
                    int data3 = rs.getInt("tr_maxseats");
                    String data4 = rs.getString("wrk_name");
                    String data5 = rs.getString("wrk_lname");
                    String data6 = rs.getString("tr_departure");
                    String data7 = rs.getString("tr_return");
                    int data8 = rs.getInt("tr_id2");
                    int data9 = rs.getInt("numofreservations");
                    int data10 = rs.getInt("availableseats");
                    Object[] row1 = { data1, data2, data3, data4, data5, data6, data7};
                    Object[] row2 = { data8, data9, data10};

                    DefaultTableModel model1 = (DefaultTableModel) jTable1.getModel();
                    DefaultTableModel model2 = (DefaultTableModel) jTable2.getModel();

                    model1.addRow(row1);
                    if(count%2==0)
                        model2.addRow(row2);

                    count++;
                }
            }else{
                JOptionPane.showMessageDialog(this, "There is no trip");
            }
  
        }catch(Exception ex){
            ex.printStackTrace();
        }
    }
   
    
