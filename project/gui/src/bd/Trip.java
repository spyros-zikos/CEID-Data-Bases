    public ArrayList<SetterTrip> tableList(){
        
        ArrayList<SetterTrip> tableList = new ArrayList<>();
        Connection con = (new GetConnection()).getConnection();
        String query = "select * from trip ";
        Statement st;
        ResultSet rs;
        
        try{
            st = con.createStatement();
            rs = st.executeQuery(query);
            SetterTrip setter;
            while(rs.next()){
                setter = new SetterTrip(rs.getInt("tr_id"),rs.getString("tr_departure"),rs.getString("tr_return"),rs.getInt("tr_maxseats"),rs.getInt("tr_cost"),rs.getInt("tr_br_code"),rs.getString("tr_gui_AT"),rs.getString("tr_drv_AT"));
                tableList.add(setter);
            }
        }catch(Exception ex){
            ex.printStackTrace();
        }
        return tableList;
    }
    
        private void DisplayTable(){
        ArrayList<SetterTrip> list = tableList();
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        Object []  row = new Object [8];
        for(int i=0; i<list.size(); i++){
            row[0]= list.get(i).getID();
            row[1]= list.get(i).getDeparture();
            row[2]= list.get(i).getReturn_date();
            row[3]= list.get(i).getMaxseats();
            row[4]= list.get(i).getCost();
            row[5]= list.get(i).getCode();
            row[6]= list.get(i).getGui_AT();
            row[7]= list.get(i).getDrv_AT();

            model.addRow(row);
        }
    }
    
    
    public void executeQuery(String query, String message){
        Connection con = (new GetConnection()).getConnection();
        Statement st;
        
        try{
            st = con.createStatement();
            if((st.executeUpdate(query))==1){
                DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
                model.setRowCount(0);
                DisplayTable();
                JOptionPane.showMessageDialog(null, "Data "+message+"Succesfully");
            }else{
                JOptionPane.showMessageDialog(null, "Data Not"+message);
            }
        }catch(Exception ex){
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null,ex.getMessage() );
        }
           
    }        
    

