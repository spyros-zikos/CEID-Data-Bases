    private void tableList(){
   
        Connection con = (new GetConnection()).getConnection();
     
        String query = "call customerReservation('"+jTextField1.getText()+"');";

        Statement st;
        ResultSet rs;
        
        try{
            st = con.createStatement();
            rs = st.executeQuery(query);
          
            while(rs.next()){ 
        
                String data1 = rs.getString("res_off_ln");
                String data2 = rs.getString("res_off_fn");
               
                int data3 = rs.getInt("res_off_off_id");
                int data4 = rs.getInt("count(*)");
             
                Object[] row1 = { data1, data2, data3, data4};
                

                DefaultTableModel model1 = (DefaultTableModel) jTable1.getModel();
              

                model1.addRow(row1);
            }
  
        }catch(Exception ex){
            ex.printStackTrace();
        }
    }

