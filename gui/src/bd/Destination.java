    public ArrayList<SetterDestination> tableList(){
        
        ArrayList<SetterDestination> tableList = new ArrayList<>();
        Connection con = (new GetConnection()).getConnection();
        String query = "select * from destination ";
        Statement st;
        ResultSet rs;
        
        try{
            st = con.createStatement();
            rs = st.executeQuery(query);
            SetterDestination setter;
            while(rs.next()){
                setter = new SetterDestination(rs.getInt("dst_id"),rs.getString("dst_name"),rs.getString("dst_descr"),rs.getString("dst_rtype"), rs.getString("dst_language"), rs.getInt("dst_location"));
                tableList.add(setter);
            }
        }catch(Exception ex){
            ex.printStackTrace();
        }
        return tableList;
    }
    
        private void DisplayTable(){
        ArrayList<SetterDestination> list = tableList();
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        Object []  row = new Object [6];
        for(int i=0; i<list.size(); i++){
            row[0]= list.get(i).getID();
            row[1]= list.get(i).getName();
            row[2]= list.get(i).getDescr();
            row[3]= list.get(i).getRtype();
            row[4]= list.get(i).getLanguage();
            if(list.get(i).getLocation() == 0){
                row[5]= "NULL";
            }else{
            row[5]= list.get(i).getLocation();
            }
            model.addRow(row);
        }
    }
    
    
    public void executeQuery(String query, String message){
        Connection con = (new GetConnection()).getConnection();
        Statement st;
        
        try{
            st = con.createStatement();
            if((st.executeUpdate(query))==1){
                DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
                model.setRowCount(0);
                DisplayTable();
                JOptionPane.showMessageDialog(null, "Data "+message+"Succesfully");
            }else{
                JOptionPane.showMessageDialog(null, "Data Not"+message);
            }
        }catch(Exception ex){
            ex.printStackTrace();
        }
           
    }     

