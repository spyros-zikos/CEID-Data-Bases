    public ArrayList<SetterBranch> tableList(){
        
        ArrayList<SetterBranch> tableList = new ArrayList<>();
        Connection con = (new GetConnection()).getConnection();
        String query = "select * from branch ";
        Statement st;
        ResultSet rs;
        
        try{
            st = con.createStatement();
            rs = st.executeQuery(query);
            SetterBranch setter;
            while(rs.next()){
                setter = new SetterBranch(rs.getInt("br_code"), rs.getString("br_street"), rs.getInt("br_num"), rs.getString("br_city"));
                tableList.add(setter);
            }
        }catch(Exception ex){
            ex.printStackTrace();
        }
        return tableList;
    }
    
        private void DisplayTable(){
        ArrayList<SetterBranch> list = tableList();
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        Object []  row = new Object [4];
        for(int i=0; i<list.size(); i++){
            row[0]= list.get(i).getCode();
            row[1]= list.get(i).getStreet();
            row[2]= list.get(i).getNum();
            row[3]= list.get(i).getCity();
            
            model.addRow(row);
        }
    }
    
    
    public void executeQuery(String query, String message){
        Connection con = (new GetConnection()).getConnection();
        Statement st;
        
        try{
            st = con.createStatement();
            if((st.executeUpdate(query))==1){
                DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
                model.setRowCount(0);
                DisplayTable();
                JOptionPane.showMessageDialog(null, "Data "+message+"Succesfully");
            }else{
                JOptionPane.showMessageDialog(null, "Data Not"+message);
            }
        }catch(Exception ex){
            ex.printStackTrace();
        }
           
    }
    
